import { z } from 'zod';

/////////////////////////////////////////
// CUSTOMERS SCHEMA
/////////////////////////////////////////

/**
 * /////////////////////////////////////////////////////////////////////////////////////////////////
 * /////////////////////////////////////////////////////////////////////////////////////////////////
 */
export const CustomersSchema = z.object({
  user_id: z.string().uuid(),
  stripe_customer_id: z.string(),
});

export type Customers = z.infer<typeof CustomersSchema>;

/////////////////////////////////////////
// CUSTOMERS PARTIAL SCHEMA
/////////////////////////////////////////

export const CustomersPartialSchema = CustomersSchema.partial();

export type CustomersPartial = z.infer<typeof CustomersPartialSchema>;

/////////////////////////////////////////
// CUSTOMERS OPTIONAL DEFAULTS SCHEMA
/////////////////////////////////////////

export const CustomersOptionalDefaultsSchema = CustomersSchema.merge(z.object({}));

export type CustomersOptionalDefaults = z.infer<typeof CustomersOptionalDefaultsSchema>;

export default CustomersSchema;
